class SettingView(LoginRequiredMixin, View):
    form_class = EditUserForm

    def get(self, request):
        form = self.form_class(instance=request.user.profile, initial={'email': request.user.email})
        return render(request, 'account/setting.html', {'form': form})

    def post(self, request):
        form = self.form_class(request.POST, instance=request.user.profile)
        if form.is_valid():
            form.save()
            request.user.email = form.cleaned_data['email']
            request.user.save()
            messages.success(request, 'Updated', 'success')
            return redirect('account:user_profile', request.user.id)



class UserFollowView(LoginRequiredMixin, View):
    def get(self, request, user_id):
        user = User.objects.get(id=user_id)
        relation = Relation.objects.filter(from_user=request.user, to_user=user)
        if relation.exists():
            messages.error(request, "شما از قبل این حساب را دنبال می کنید.", 'danger')
        else:
            Relation(from_user=request.user, to_user=user).save()
            messages.success(request, "شما الان این حساب را دنبال می کنید.", 'success')
        return redirect('account:user_profile', user.id)


class UserUnfollowView(LoginRequiredMixin, View):
    def get(self, request, user_id):
        user = User.objects.get(id=user_id)
        relation = Relation.objects.filter(from_user=request.user, to_user=user)
        if relation.exists():
            relation.delete()
            messages.success(request, "شما از الان این حساب را دنبال نمی کنید.", 'success')
        else:
            messages.error(request, "شما این حساب را دانبال نمی کردید.", 'danger')
        return redirect("account:user_profile", user.id)
